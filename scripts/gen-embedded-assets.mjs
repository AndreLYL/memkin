// Generates src/embedded-assets.generated.ts from schema.sql and the extractor
// prompt markdown. Inlining these as string constants lets them survive bundling:
// the tsc-built dist/, the `bun build --compile` single-file binary, and `bun src`
// all read the same embedded text with no runtime filesystem dependency.
//
// Run via `bun run gen:assets` (also runs automatically before `build`).

import { readFileSync, readdirSync, statSync, writeFileSync } from "node:fs";
import { join, relative } from "node:path";

const root = process.cwd();
const schemaPath = join(root, "src", "store", "schema.sql");
const promptsDir = join(root, "src", "extractors", "prompts");
const outPath = join(root, "src", "embedded-assets.generated.ts");

function walkMarkdown(dir) {
  const found = [];
  for (const name of readdirSync(dir)) {
    const full = join(dir, name);
    if (statSync(full).isDirectory()) {
      found.push(...walkMarkdown(full));
    } else if (full.endsWith(".md")) {
      found.push(full);
    }
  }
  return found;
}

const version = JSON.parse(readFileSync(join(root, "package.json"), "utf-8")).version;

const schema = readFileSync(schemaPath, "utf-8");

const prompts = {};
for (const file of walkMarkdown(promptsDir).sort()) {
  const key = relative(promptsDir, file).split("\\").join("/");
  prompts[key] = readFileSync(file, "utf-8");
}

const banner =
  "// AUTO-GENERATED by scripts/gen-embedded-assets.mjs — do not edit by hand.\n" +
  "// Source: src/store/schema.sql + src/extractors/prompts/**/*.md\n" +
  "// Regenerate with `bun run gen:assets`.\n";

const body =
  `${banner}\n` +
  `export const VERSION: string = ${JSON.stringify(version)};\n\n` +
  `export const SCHEMA_SQL: string = ${JSON.stringify(schema)};\n\n` +
  `export const PROMPTS: Record<string, string> = ${JSON.stringify(prompts, null, 2)};\n`;

writeFileSync(outPath, body, "utf-8");
console.log(
  `Generated ${relative(root, outPath)} (schema + ${Object.keys(prompts).length} prompts)`,
);
